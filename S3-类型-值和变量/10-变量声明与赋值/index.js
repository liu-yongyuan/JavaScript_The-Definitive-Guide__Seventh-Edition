/**
 * 计算机编程中最基本的一个基数就是使用名字(或标识符)表示值.
 * 绑定名字和值为我们提供了一种引用值和在程序中使用值的方式.
 * 
 * 在 JavaScript 中使用变量或常量前, 必须先声明它. 在 ES6
 * 及之后的版本中, 这是通过 let 和 const 关键字完成的. 在
 * Es6 以前, 变量是通过 var 声明的
 */

/**
 * ES6 及之后
 */
let variableA = 10; // let 关键字, 变量声明
const variableB = 10; // const 关键字, 常量声明
var variable = 10; // var 关键字, 变量声明